package com.jpym.ymfrontgm.model;

import com.jpym.ymfrontgm.util.Column;
import lombok.Getter;
import lombok.Setter;

@Getter
@Setter
public class TradeFeeModel {
    @Column( "benchmarkrates" )
    private Number benchmarkrates; //基准费率
    @Column( "charge" )
    private Number charge; //手续费
    @Column( "chargerate" )
    private Number chargerate; //手续费率
    @Column( "discount" )
    private Number discount; //折扣率
    @Column( "executionrate" )
    private Number executionrate; //执行费率
    @Column( "precharge" )
    private Number precharge;//原手续费
}
